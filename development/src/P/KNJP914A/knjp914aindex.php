<?php

require_once('for_php7.php');

require_once('knjp914aModel.inc');
require_once('knjp914aQuery.inc');

class knjp914aController extends Controller
{
    public $ModelClassName = "knjp914aModel";
    public $ProgramID      = "KNJP914A";

    public function main()
    {
        $sessionInstance =& Model::getModel($this);
        while (true) {
            switch (trim($sessionInstance->cmd)) {
                case "":
                case "main":
                    $this->callView("knjp914aForm1");
                    break 2;
                case "update":
                    $sessionInstance->getUpdateModel();
                    //変更済みの場合は詳細画面に戻る
                    $sessionInstance->setCmd("main");
                    break 1;
                case "error":
                    $this->callView("error");
                    break 2;
                default:
                    $sessionInstance->setError(new PEAR_Error("未対応のアクション{$sessionInstance->cmd}です"));
                    $this->callView("error");
                    break 2;
            }
        }
    }
}
$knjp914aCtl = new knjp914aController();
