<?php

require_once('for_php7.php');

require_once('knjl060mModel.inc');
require_once('knjl060mQuery.inc');

class knjl060mController extends Controller {
    var $ModelClassName = "knjl060mModel";
    var $ProgramID      = "KNJL060M";

    function main()
    {
        $sessionInstance =& Model::getModel($this);
        while ( true ) {
            switch (trim($sessionInstance->cmd)) {
                case "exec":
                    $sessionInstance->getExecModel();
                    $sessionInstance->setCmd("main");
                    break 1;
                case "":
                case "main":
                    $sessionInstance->getMainModel();
                    $this->callView("knjl060mForm1");
                    break 2;
                case "error":
                    $this->callView("error");
                    break 2;
                default:
                    $sessionInstance->setError(new PEAR_Error("未対応のアクション{$sessionInstance->cmd}です"));
                    $this->callView("error");
                    break 2;
            }
            
        }
    }
}
$knjl060mCtl = new knjl060mController;
?>
