function btn_submit(cmd, schno, sem, rowNo) {

    if (cmd == 'reset') {
        if (!confirm('{rval MSG106}')) return false;
    }

    if (cmd == 'teikei' && schno != "" && sem != "" && rowNo != "") {
        loadwindow('knjd132vindex.php?cmd='+cmd+'&SEM='+sem+'&SCHNO='+schno+'&ROWNO='+rowNo, event.clientX + function () {var scrollX = document.documentElement.scrollLeft || document.body.scrollLeft;return scrollX;}(), event.clientY + function () {var scrollY = document.documentElement.scrollTop || document.body.scrollTop;return scrollY;}(), 650, 450);

        return true;
    }

    //サブミット
    document.forms[0].cmd.value = cmd;
    document.forms[0].submit();
    return false;
}

