function btn_submit(cmd) {
    document.forms[0].cmd.value = cmd;
    document.forms[0].submit();
    return false;
}

function newwin(SERVLET_URL) {
    if (document.forms[0].CLASS_SELECTED.length == 0) {
        alert('{rval MSG916}');
        return false;
    }

    if (document.forms[0].EDATE.value == '') {
        alert("日付が不正です。");
        document.forms[0].EDATE.focus();
        return false;
    }

    var sdate = document.forms[0].SDATE.value;              //出欠集計開始日付
    var edate = document.forms[0].EDATE.value;              //出欠集計終了日付
    var seme_edate = document.forms[0].SEME_EDATE.value;    //学期終了日付

    if (sdate > edate || seme_edate < edate) {
        alert("日付が範囲外です。（" + sdate + "～" + seme_edate + "）");
        document.forms[0].EDATE.focus();
        return;
    }

    if (document.forms[0].RANK_NO.value == '') {
        alert("成績上位者が未入力です。");
        return;
    }
    var rankNo = document.forms[0].RANK_NO.value;
    if (6 < rankNo) {
        alert("成績上位者が範囲外です。0～6までの範囲内で入力して下さい。");
        return;
    }
    if (document.forms[0].BAD_SCORE.value == '' || document.forms[0].BAD_SUBCNT.value == '') {
        alert("成績欠点が未入力です。");
        return;
    }

    for (var i = 0; i < document.forms[0].CLASS_NAME.length; i++) {
        document.forms[0].CLASS_NAME.options[i].selected = 0;
    }

    for (var i = 0; i < document.forms[0].CLASS_SELECTED.length; i++) {
        document.forms[0].CLASS_SELECTED.options[i].selected = 1;
    }

    action = document.forms[0].action;
    target = document.forms[0].target;

    document.forms[0].action = SERVLET_URL +"/KNJD";
    document.forms[0].target = "_blank";
    document.forms[0].submit();

    document.forms[0].action = action;
    document.forms[0].target = target;
}

