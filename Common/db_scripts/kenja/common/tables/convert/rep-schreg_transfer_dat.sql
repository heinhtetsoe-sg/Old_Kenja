DROP VIEW V_ABROAD_DAT
DROP TABLE SCHREG_TRANSFER_DAT_OLD
RENAME TABLE SCHREG_TRANSFER_DAT TO SCHREG_TRANSFER_DAT_OLD
CREATE TABLE SCHREG_TRANSFER_DAT \
    (SCHREGNO         VARCHAR(8)    NOT NULL, \
     TRANSFERCD       VARCHAR(2)    NOT NULL, \
     TRANSFER_SDATE   DATE          NOT NULL, \
     TRANSFER_EDATE   DATE, \
     TRANSFERREASON   VARCHAR(75), \
     TRANSFERPLACE    VARCHAR(60), \
     TRANSFERADDR     VARCHAR(75), \
     ABROAD_CLASSDAYS SMALLINT, \
     ABROAD_CREDITS   SMALLINT, \
     ABROAD_PRINT_DROP_REGD VARCHAR(1), \
     REMARK1          VARCHAR(90), \
     REMARK2          VARCHAR(90), \
     REGISTERCD       VARCHAR(10), \
     UPDATED          TIMESTAMP DEFAULT CURRENT TIMESTAMP \
    )IN USR1DMS INDEX IN IDX1DMS

INSERT INTO SCHREG_TRANSFER_DAT \
