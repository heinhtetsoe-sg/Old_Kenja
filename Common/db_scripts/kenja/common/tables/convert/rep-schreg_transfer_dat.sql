     SCHREGNO, \
     TRANSFERCD, \
     TRANSFER_SDATE, \
     TRANSFER_EDATE, \
     TRANSFERREASON, \
     TRANSFERPLACE, \
     TRANSFERADDR, \
     ABROAD_CLASSDAYS, \
     ABROAD_CREDITS, \
     CAST(NULL AS VARCHAR(1)) AS ABROAD_PRINT_DROP_REGD, \
     REMARK1, \
     REMARK2, \
     REGISTERCD, \
     UPDATED \
    FROM \
        SCHREG_TRANSFER_DAT_OLD

